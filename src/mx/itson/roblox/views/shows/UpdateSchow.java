    /**
     * Fills the tblMovies table
     */
    public void updateTableMovies(){
        DefaultTableModel model = (DefaultTableModel) tblMovies.getModel();
        model.setNumRows(0);
        int nList = 0;
        for(Movie m : movies){
            nList ++;
            model.addRow(new Object[]{
                m.getId(), m.getName()
            });
        }
    }
    
    /**
     * Fills the tblEmployees table
     */
    public void updateTableEmployee(){
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        model.setNumRows(0);
        int nList = 0;
        for(Employee e : employees){
            nList ++;
            model.addRow(new Object[]{
                e.getId(), e.getName()
            });
        }
    }
    
    /**
     * Fills the tblRooms table
     */
    public void updateTableRooms(){
        DefaultTableModel model = (DefaultTableModel) tblRooms.getModel();
        model.setNumRows(0);
        int nList = 0;
        for(Room r : rooms){
            nList ++;
            model.addRow(new Object[]{
                r.getId()
            });
        }
    }
    
