    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked

        String schedule=txfSchedule.getText();
        String scheduleEmpty=schedule.replaceAll(" ", "");
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date();
        try {
            date = dateFormat.parse(txfDate.getText());
        } catch (ParseException ex) {
            //           Logger.getLogger(CreateShow.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Ocurrio un error con la fecha");
        }

        if(!(employeeId==0 || movieId==0 || roomId==0 || scheduleEmpty.length()==0 || txfDate.getText().length()==0)){

            if(ShowModel.Update(show.getId(),employeeId, movieId, roomId, schedule, date)){
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, "No se ha podido registrar al usuario");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Favor de llenar todos los campos");
        }
    }//GEN-LAST:event_btnAddMouseClicked

